package org.richfaces;

import java.io.Serializable;

/**
 * @author Nick Belaevski
 * @since 3.3.0
 */
public class ConvertableDemoBean implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 6202523836189784862L;

	private String name;
	
	private int value;

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @return the value
	 */
	public int getValue() {
		return value;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#hashCode()
	 */
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		result = prime * result + value;
		return result;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#equals(java.lang.Object)
	 */
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		ConvertableDemoBean other = (ConvertableDemoBean) obj;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		if (value != other.value)
			return false;
		return true;
	}

	public ConvertableDemoBean(String name, int value) {
		super();
		this.name = name;
		this.value = value;
	}

}
